#################3 #打印5遍hello word

#1、定义一个整数变量，记录循环次数
i = 1

#2、开始循环
while i <= 5:

    #循环内执行的代码
    print("hello word")

    #处理计数器
    i = i + 1

#3、观察一下循环结束后，计数器i的数值是多少（debug模式，按F8调试）
print ("循环结束后，i = %d" % i)


